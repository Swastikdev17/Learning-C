#include <stdio.h>

int main() {
    int a = 5;
    int b = 5;
    int c = a+b; // The '+' sign is the addition operator and so are '-','*' and '/'
    printf("The value of a is %d, the value of b is %d and the sum of a and b is %d",a,b,c);
    float d = 9%5;
    printf("The value of 9/5 is:%f",d);
    return 0;
}